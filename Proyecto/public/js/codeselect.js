function seleccionado() {
    var opt = $("#opcion").val();
    // alert(opt);
    if (opt == "1" || opt == "2") {
        $("#categoria").show();
    }
}
function showContent() {
    element = document.getElementById("content");
    check = document.getElementById("check");
    if (check.checked) {
        element.style.display = "block";
    } else {
        element.style.display = "none";
    }
}
